# EJERCICIO 02 (CONVERTIR LISTA A TUPLA)

lista = [1,2,3]
tupla = tuple(lista)
print(f"Lista'{lista}'a tupla: {tupla}")

tupla_rango = tuple(range(5))
print(f"El rango de (5) a tupla: {tupla_rango}")